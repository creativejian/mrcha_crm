// ⚠️ **알려진 한계(2026-07-23 확인, 별건)**: 10자리는 3-3-4로 자른다 — 서울 유선번호가
// `0212345678` → `021-234-5678`로 잘못 나온다(지역번호 2자리를 모른다). 실 데이터는 현재
// 전부 11자리 휴대폰이라(crm.phone 20 · phone_secondary 1 · profiles 7, 10자리 0건) 실영향이
// 없어 이번 슬라이스 범위 밖으로 뒀다. 고칠 땐 `02`만 2자리로 끊으면 된다 — **화면 표시도
// 함께 바뀐다**는 점을 감안할 것. 그때까지 이 함수 테스트에 유선번호를 넣지 말 것(잘못된
// 동작을 테스트가 박제한다).
export function formatPhone(raw: string | null): string {
  const d = (raw ?? "").replace(/\D/g, "");
  if (d.length === 11) return `${d.slice(0, 3)}-${d.slice(3, 7)}-${d.slice(7)}`;
  if (d.length === 10) return `${d.slice(0, 3)}-${d.slice(3, 6)}-${d.slice(6)}`;
  return raw ?? "";
}
