// 서버도 이 함수가 필요했다. 손으로 복제하면 두 표기가 조용히 갈린다.
//
// 계약: DB엔 **숫자만** 저장하고(서버 zod digits 정규화 — 전화번호 소유권 계약 `#276`),
// 화면·AI 답변에서만 하이픈으로 보여준다. 입력이 하이픈 포함이어도 먼저 숫자만 추출하므로
// digits/hyphen 둘 다 안전(전환기 호환).
//
// 지역번호 규칙: **서울만 2자리(`02`)**이고 나머지는 3자리(031·051 …)다. 휴대폰(010)·인터넷전화
// (070)는 3자리 접두로 같은 취급.
// ⚠️ 구 구현은 10자리를 **일괄 3-3-4**로 잘라 서울 번호가 `0212345678` → `021-234-5678`이 됐다
// (2026-07-23 `#336` 작업 중 발견 → `#339`에서 수정). 실 데이터가 전부 11자리 휴대폰이라
// (10자리 0건 실측) 화면에 드러난 적은 없지만, **화면과 업무 AI가 이 함수를 공유**하므로
// 틀리면 두 곳이 함께 틀린다.
// 길이 규칙이 없는 입력은 **원문 그대로** 반환한다 — 임의로 자르면 없는 번호를 만들어낸다.
export function formatPhone(raw: string | null): string {
  const d = (raw ?? "").replace(/\D/g, "");
  // 서울: 2 + (3|4) + 4 → 9자리 또는 10자리.
  if (d.startsWith("02") && (d.length === 9 || d.length === 10)) {
    return `${d.slice(0, 2)}-${d.slice(2, d.length - 4)}-${d.slice(-4)}`;
  }
  if (d.length === 11) return `${d.slice(0, 3)}-${d.slice(3, 7)}-${d.slice(7)}`;
  if (d.length === 10) return `${d.slice(0, 3)}-${d.slice(3, 6)}-${d.slice(6)}`;
  return raw ?? "";
}
