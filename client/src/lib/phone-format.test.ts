import { describe, expect, it } from "vitest";

import { formatPhone } from "./phone-format";

// 표시 포맷 SSOT의 자체 테스트(2026-07-23 신설). 이 함수는 화면(고객 목록·상세)과 업무 AI 답변이
// **공유**하므로, 여기가 틀리면 두 곳이 함께 틀린다. 그런데 분리 전(`lib/customers.ts` 시절)부터
// 전용 테스트가 0개였고, 그래서 10자리 오포맷이 오래 눈에 띄지 않았다.
describe("formatPhone", () => {
  // 11자리 휴대폰 = 3-4-4. 실 데이터는 현재 전부 이 형태다(crm.phone 20 · phone_secondary 1 ·
  // profiles.phone_number 7 — 2026-07-23 실측, 10자리 0건).
  it("11자리 휴대폰은 3-4-4", () => {
    expect(formatPhone("01012345678")).toBe("010-1234-5678");
    expect(formatPhone("01054868279")).toBe("010-5486-8279");
  });

  // ⚠️ 이 슬라이스의 본체 — 구 구현은 10자리를 **일괄 3-3-4**로 잘라 서울 번호가
  // `021-234-5678`이 됐다(지역번호가 2자리인 걸 몰랐다). 02만 2자리다.
  it("서울(02) 유선번호는 2자리 지역번호로 끊는다", () => {
    expect(formatPhone("0212345678")).toBe("02-1234-5678"); // 10자리 서울
    expect(formatPhone("021234567")).toBe("02-123-4567"); // 9자리 서울(구 국번 7자리)
  });

  // 그 외 지역번호(031·051…)는 3자리 — 10자리면 3-3-4다.
  it("서울 외 지역번호는 3자리 유지", () => {
    expect(formatPhone("0316231234")).toBe("031-623-1234");
    expect(formatPhone("05112345678")).toBe("051-1234-5678"); // 11자리 유선도 3-4-4
  });

  // 050·070 등 비지역 번호가 02로 시작하지 않는 한 기존 규칙 그대로.
  it("070 인터넷전화는 3-4-4", () => {
    expect(formatPhone("07012345678")).toBe("070-1234-5678");
  });

  // 저장은 digits지만 입력이 하이픈 포함이어도 안전해야 한다(전환기 호환 — 구 주석의 계약).
  it("하이픈이 섞여 들어와도 숫자만 뽑아 다시 포맷한다", () => {
    expect(formatPhone("010-1234-5678")).toBe("010-1234-5678");
    expect(formatPhone("02-1234-5678")).toBe("02-1234-5678");
  });

  // 알 수 없는 길이는 **원문 그대로** 반환한다(임의로 자르면 없는 번호를 만들어낸다).
  it("규칙에 없는 길이는 원문 유지", () => {
    expect(formatPhone("12345")).toBe("12345");
    expect(formatPhone(null)).toBe("");
    expect(formatPhone("")).toBe("");
  });
});
